list_1: list = ['bear', 'milk', 'eg', 'eg', 'eg', 'eg']

# С помощью цикла "while" удоляем элемент списка необходимое количество раз. Для того чтобы удолить все одиноковые
# елементы в списке, мы в условие "While" используем условие 0 < (количество диноковых елементов в списке)
while 0 < list_1.count('eg'):
    # удоляем елемент из списка
    list_1.remove('eg')

print(list_1)
