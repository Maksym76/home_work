# Запрашиваем у пользователя пароль
type_password: str = input("Введите пароль: ")

while True:
    # Просим пользователя повторить пароль
    repeat_password: str = input("Повторите пароль: ")
    # Если пароль совпал, выводим сообщение "Вы вошли!"
    if type_password == repeat_password:
        print("Вы вошли!")
        break
    # Если пароль совпал, выводим сообщение "Неверный пароль, пожалуйста повторите."
    else:
        print("Неверный пароль, полжалуйста повторите.")
