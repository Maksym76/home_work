# Вводим текст и превращаем его в список
text: list = list(input('ведите текст: '))

# Создаем словарь с ключами из нашего списка "Текст"
text_dict = {}.fromkeys(text)

# Создаем значение для наших ключей. Значения будут равны тому колличетву сколько знак встречается в списке "Текст"
for k in text_dict:
    text_dict[k] = text.count(k)

print(text_dict)
