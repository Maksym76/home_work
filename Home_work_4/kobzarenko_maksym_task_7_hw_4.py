# Создали список содержащий кортеж
x = [(1, 2), (2, 3, 5), (3, 4), (2, 3, 4, 2)]
# Создаем новую переменную куда будем ложить преобразованные кортежи из переменной "х"
y = []
# Проходим по каждому кортежу в переменной "х" и преобразовываем в список. Добаваляем полученые списки в переменную "хх"
for i in x:
    i = [*i]  # or list(i)
    y += [i]

print(y)
