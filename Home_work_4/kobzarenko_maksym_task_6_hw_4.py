#  Водим значения через запятую. Разбивает строку на список по ",". Затем с помощью функции 'map' меняем тип данных в
# списке на числовые. Затем меняем class 'list' на 'tuple'
value = tuple(map(int, input("Ведите цифры через зяпятую: ").split(',')))

# Выводим 1-й и 2-й элемент кортежа с помощью индексации
print(value[0:2])
